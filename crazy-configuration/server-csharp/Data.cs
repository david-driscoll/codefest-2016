        public static IEnumerable<string> GetNamesForSection(string name)
        {
            return _names
                .Where(x => (x.section ?? "").Equals(name, StringComparison.OrdinalIgnoreCase))
                .Select(x => x.name)
                .OrderBy(x => x);
        }

        public static IEnumerable<string> GetSections()
        {
            return _names
                .Select(x => x.section)
                .Distinct()
                .OrderBy(x => x);
        }
